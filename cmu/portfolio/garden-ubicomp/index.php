    <h6>The GardenPot system provides gardeners information about their gardens even when they are not present in the garden.</h6>
    <p>We created a system that brings information about the community garden into the home. The GardenPot system consists of two main parts. In the garden, moisture, temperature, and pressure sensors record both human and plant activity. This information is relayed to each gardener's own in-home GardenPot. The GardenPot displays this information in a natural form without resorting to screens and obvious technology. </p>
