    <h5 class="red">Speed Dating</h5>
    <h6>Using rapid testing to explore the social boundaries of ubiquitous computing</h6>
    <p>We speed dated with three gardeners to test what types of social activity and technology they would find acceptable in the garden. The speed dating process involved the team creating ten different scenarios to test how we could introduce technology into the garden. We took those storyboards, sat down with gardeners, walked them through the scenario, and watched their reactions.
    </p>
  <p>
      The results of speed-dating gave our design choices new specicifity, and made our idea gardener much more concrete. The speed dating sessions made it clear that our best chance for creating a useful innovation was in the following areas: Connecting gardeners to their fellow gardners, connecting gardeners with the wider neighborhood, and enabling gardeners to more easily share ripe food with one another.
    </p>
      
      
   
